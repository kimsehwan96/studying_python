# 직사각형의 경계선까지 최소거리는.. x 의 왼쪽, 오른쪽 , y의 위쪽, 아래쪽 모두 비교해서 젤 작은거 고

def find_min(x,y,w,h):
    a = abs(x - w)
    b = abs(y - h)
    c = x
    d = y
    li = [a,b,c,d]
    li.sort()
    print(li[0])

if __name__ == "__main__":
    x,y,w,h = map(int, input().split())
    find_min(x,y,w,h)