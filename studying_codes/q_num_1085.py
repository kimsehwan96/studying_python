# 직사각형 
# 왼쪽 아래 꼭지점은 0,0 오른쪽 위는 w,h (x좌표, y좌표)

#예제입력 x,y w,h  -> 6, 2, 10, 3
# 한수는 현재 6,2에있고, 오른쪽 위 꼭지점은 10,3 
# 그렇다는건... 오른쪽 위 꼭지점이 10, 3이라는건 

